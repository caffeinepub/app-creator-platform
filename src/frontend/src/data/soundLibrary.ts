// ── SynthParams: flat structure compatible with useAudioEngine ────────────────
export interface SynthParams {
  oscillatorType?: OscillatorType;
  frequency?: number;
  frequencyEnd?: number;
  duration?: number;
  gainStart?: number;
  gainEnd?: number;
  detune?: number;
  filterType?: BiquadFilterType;
  filterFreq?: number;
  filterQ?: number;
  delayTime?: number;
  delayFeedback?: number;
  reverbAmount?: number;
  distortion?: number;
  lfoRate?: number;
  lfoDepth?: number;
  attack?: number;
  decay?: number;
  sustain?: number;
  release?: number;
  noiseType?: "white" | "pink" | "brown";
  harmonics?: number[];
  sweepStart?: number;
  sweepEnd?: number;
  sweepTime?: number;
  pulseWidth?: number;
  chorus?: boolean;
  bitcrush?: number;
  // New fields for modulation / pitch envelope
  pitchEnvelope?: { startFreq: number; endFreq: number; time: number };
  modulation?: { type: OscillatorType; frequency: number; depth: number };
}

export interface UploadedData {
  base64Data: string;
  mimeType: string;
}

export interface SoundEntry {
  id: string;
  name: string;
  category: string;
  emoji: string;
  description: string;
  synthParams: SynthParams;
  uploadedData?: UploadedData;
}

// Legacy SoundCategory type kept for backward compatibility
export type SoundCategory = string;

